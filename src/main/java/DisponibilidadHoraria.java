package main.java;

public class DisponibilidadHoraria{
	private Number Dia;
	private Number HoraDesde;
	private Number MinutoDesde;
	private Number HoraHasta;
	private Number MinutoHasta;
	public Number getDia() {
		return Dia;
	}
	public void setDia(Number dia) {
		Dia = dia;
	}
	public Number getHoraDesde() {
		return HoraDesde;
	}
	public void setHoraDesde(Number horaDesde) {
		HoraDesde = horaDesde;
	}
	public Number getMinutoDesde() {
		return MinutoDesde;
	}
	public void setMinutoDesde(Number minutoDesde) {
		MinutoDesde = minutoDesde;
	}
	public Number getHoraHasta() {
		return HoraHasta;
	}
	public void setHoraHasta(Number horaHasta) {
		HoraHasta = horaHasta;
	}
	public Number getMinutoHasta() {
		return MinutoHasta;
	}
	public void setMinutoHasta(Number minutoHasta) {
		MinutoHasta = minutoHasta;
	}
}